/// A "host adapter" config — the marker file declares one of these
/// per JSX symbol that should be backed by a stateful, controller-
/// owning Dart wrapper rather than a pure props-in-widget-out adapter.
///
/// Used for widgets whose constructor REQUIRES a runtime-constructed
/// controller (CameraController, VideoPlayerController, animation
/// controllers, etc.) — primitives over the bridge can't represent
/// those objects, so codegen synthesizes a StatefulWidget that:
///
///   1. Receives ordinary primitive props from JSX.
///   2. Calls a dev-provided factory function in `initState` to
///      create the controller (sync or async).
///   3. Holds it in State until `dispose()`.
///   4. Disposes the controller cleanly on unmount.
///   5. Renders the underlying widget once the controller is ready.
///
/// One YAML entry → one synthesized `<JsxName>` JSX symbol → one
/// generated `_<JsxName>Host` StatefulWidget under the hood.
class HostConfig {
  /// JSX-facing symbol name (the key in `hosts:` in the marker file).
  /// E.g. `Camera`, which lowers to the registry key `camera` and
  /// renders the synthesized `_CameraHost` StatefulWidget.
  final String jsxName;

  /// The class name of the underlying Widget the host wraps —
  /// resolved by the analyzer below (must be available in the same
  /// scope as the generated adapter, typically via the wrapped pub
  /// package's canonical import).
  final String wrappedWidgetName;

  /// The dev's factory function (returns the controller). Either a
  /// plain `Foo` or a `Future<Foo>` — codegen detects which and
  /// emits `await` accordingly.
  final ExecutableElement2 factoryFn;

  /// Import URI for the factory function — emitted as an `import`
  /// in the generated file so the synthesized `_CameraHost` can call
  /// it. Typically a `package:` URI pointing at a file in the
  /// consumer's project.
  final String factoryImport;

  /// Import URI for the wrapped Widget class — the synthesized
  /// `_CameraHost.build()` constructs it directly, so the import has
  /// to be in scope.
  final String wrappedWidgetImport;

  /// How the wrapped widget receives the controller. Most "viewport"
  /// widgets (CameraPreview, VideoPlayer) take it as the FIRST
  /// POSITIONAL argument — that's the default. Some packages use a
  /// NAMED argument instead (WebView's `controller:`, form-style
  /// widgets' `textController:`); the dev declares the param name
  /// in the marker file's `controllerProp:` field.
  ///
  ///   null  → positional (default): `WrappedWidget(controller)`
  ///   "foo" → named:                `WrappedWidget(foo: controller)`
  final String? controllerProp;

  HostConfig({
    required this.jsxName,
    required this.wrappedWidgetName,
    required this.factoryFn,
    required this.factoryImport,
    required this.wrappedWidgetImport,
    this.controllerProp,
  });
}

/// One Widget class the generator considered, with the result of that
/// consideration: emitted code OR a skip reason. Returned alongside
/// the final source so callers can surface warnings to the dev.
class GeneratedWidget {
  final String className;
  final String registryKey;
  final String? skipReason; // null = generated; non-null = skipped

  /// Names of optional constructor params that were unsupported and
  /// thus OMITTED from the generated adapter (the constructor's own
  /// default takes effect). Only populated when [skipReason] is null
  /// — a skipped widget has no per-param breakdown. Useful for the
  /// CLI to tell the dev "QrImageView: 12 props mapped, 3 omitted
  /// (padding, embeddedImage, errorStateBuilder)".
  final List<String> omittedOptionalParams;

  GeneratedWidget(
    this.className,
    this.registryKey, {
    this.skipReason,
    this.omittedOptionalParams = const [],
  });
}

class GenerationResult {
  final String source;
  final List<GeneratedWidget> widgets;

  GenerationResult(this.source, this.widgets);

  /// Widgets that DID make it into the output.
  Iterable<GeneratedWidget> get generated =>
      widgets.where((w) => w.skipReason == null);

  /// Widgets that DIDN'T — name + skip reason for diagnostics.
  Iterable<GeneratedWidget> get skipped =>
      widgets.where((w) => w.skipReason != null);
}

/// Run the generator against [units]. [sourceRelativeImports] is the
/// list of import paths to inline into the generated file's header —
/// typically one per input unit, relative to where the generated file
/// will be written.
///
/// [hosts], if non-empty, declares "host adapters" — synthesized
/// StatefulWidget wrappers that own a controller-typed object for
/// each entry, instead of the usual props-in/widget-out adapter. See
/// [HostConfig] for the full rationale + the emission shape.
GenerationResult generate({
  required List<ResolvedUnitResult> units,
  required List<String> sourceRelativeImports,
  List<HostConfig> hosts = const [],
}) {
  final widgets = <GeneratedWidget>[];
  final adapterBodies = <String>[];
  final registryCalls = <String>[];
  // Extra imports an encoding needs beyond the standard framework
  // imports (e.g. widget-child encoding wants SkalNode from root.dart).
  // Deduped before emission.
  final extraImports = <String>{};

  // Track which units contributed at least one GENERATED widget. Units
  // with no widgets (helper-only files in a multi-file scan) or only
  // skipped widgets don't need to be imported by the generated file —
  // emitting unused imports just produces dead lint warnings in the
  // consumer's project.
  final contributingUnitIdx = <int>{};

  // Classes that have a host config attached. The regular widget walk
  // SKIPS these — otherwise we'd emit a broken `_build_CameraPreview`
  // (the codegen can't encode CameraController) alongside the
  // synthesized `_build_Camera` host. Whoever declared the host owns
  // how the underlying widget gets constructed.
  final hostedWidgetNames = {for (final h in hosts) h.wrappedWidgetName};

  for (var i = 0; i < units.length; i++) {
    final unit = units[i];
    final classes = _topLevelWidgetClasses(unit);
    for (final cls in classes) {
      // Host targets bypass the regular emission. See [hosts] in
      // generate()'s contract: the synthesized host below replaces
      // any auto-generated adapter for the same class.
      if (hostedWidgetNames.contains(cls.name3)) continue;
      final ctors = _eligibleConstructors(cls);
      if (ctors.isEmpty) {
        // Class has no usable constructor (all private, or somehow
        // no constructors at all). Record a skip so the dev sees the
        // class was considered but produced nothing.
        widgets.add(GeneratedWidget(
          cls.name3 ?? '',
          _camelCase(cls.name3 ?? ''),
          skipReason: 'no public constructors',
        ));
        continue;
      }
      for (final ctor in ctors) {
        final result = _emitAdapter(cls, ctor);
        widgets.add(result.summary);
        if (result.body != null) {
          adapterBodies.add(result.body!);
          contributingUnitIdx.add(i);
          extraImports.addAll(result.requiredImports);
        }
        if (result.registryLine != null) {
          registryCalls.add(result.registryLine!);
        }
      }
    }
  }

  // ── Host adapters ─────────────────────────────────────────────────
  //
  // For each declared HostConfig, emit a private StatefulWidget that
  // owns a controller-typed object, plus the `_build_<JsxName>`
  // function the registry calls. The host wraps the underlying widget
  // (cls.name3 == h.wrappedWidgetName) without trying to encode the
  // controller across the bridge.
  //
  // Each host config contributes TWO imports to the generated file:
  // the wrapped widget's package (so we can construct it) and the
  // factory function's file (so we can call it). Both go through
  // [extraImports] for deduplication.
  for (final host in hosts) {
    final result = _emitHostAdapter(host);
    widgets.add(result.summary);
    if (result.body != null) {
      adapterBodies.add(result.body!);
      extraImports.addAll(result.requiredImports);
    }
    if (result.registryLine != null) {
      registryCalls.add(result.registryLine!);
    }
  }

  // Basename-only so the output is stable across machines: a
  // committed generated file shouldn't have an absolute path in
  // its header. Filtered to CONTRIBUTING units (those that produced
  // an adapter) — listing every walked .dart file in a package's
  // lib/ would just be noise.
  final sourceList = [
    for (var i = 0; i < units.length; i++)
      if (contributingUnitIdx.contains(i)) p.basename(units[i].path)
  ].join(', ');
  final buffer = StringBuffer()
    ..writeln('// GENERATED by skal_codegen — do not edit by hand.')
    ..writeln('//')
    ..writeln('// Source: $sourceList')
    ..writeln('//')
    ..writeln('// One adapter per Widget class found in the source. Each adapter reads')
    ..writeln("// its constructor's named parameters from the bridge's custom-prop bag")
    ..writeln('// via NodeState.getCustomPropX(name, default), then constructs the')
    ..writeln("// real Flutter widget. The default value comes from the constructor's")
    ..writeln('// own default — if the JSX consumer omits a prop, they get the same')
    ..writeln('// behaviour as a direct Dart caller would.')
    // Lint suppressions for machine-emitted code. Each is opt-in for a
    // specific generator pattern:
    //
    //   • non_constant_identifier_names — `_build_FooBar` follows the
    //     synthesized symbol name (PascalCase) for traceability,
    //     deliberately not lowerCamelCase.
    //   • sort_child_properties_last — params are emitted in the
    //     CONSTRUCTOR's declared order (so default-value mapping stays
    //     trivial), which can put `child`/`children` before primitive
    //     siblings. Reordering would couple the generator to lint
    //     style rather than to source-of-truth semantics.
    //   • unused_import — unlikely (we filter to contributing units)
    //     but cheap to suppress for resilience against future changes
    //     to that filtering logic.
    ..writeln('//')
    ..writeln('// ignore_for_file: non_constant_identifier_names, '
        'sort_child_properties_last, unused_import')
    ..writeln()
    ..writeln("import 'package:flutter/material.dart';")
    ..writeln("import 'package:skal_flutter/skal/bridge.dart';")
    ..writeln("import 'package:skal_flutter/skal/node_state.dart';")
    ..writeln("import 'package:skal_flutter/skal/registry.dart';");
  // Conditional extra framework imports — one per encoding that
  // requested it. Sorted for deterministic output across runs.
  for (final imp in extraImports.toList()..sort()) {
    buffer.writeln("import '$imp';");
  }
  buffer.writeln();
  // Dedupe imports: when multiple input files come from the same
  // pub-cache package, the CLI maps them all to a single `package:`
  // entry-point URI. Emitting that twice is a Dart compile error.
  final emittedImports = <String>{};
  for (var i = 0; i < sourceRelativeImports.length; i++) {
    if (!contributingUnitIdx.contains(i)) continue;
    final imp = sourceRelativeImports[i];
    if (!emittedImports.add(imp)) continue;
    buffer.writeln("import '$imp';");
  }
  buffer.writeln();

  for (final body in adapterBodies) {
    buffer.writeln(body);
    buffer.writeln();
  }

  buffer.writeln('void registerAll() {');
  for (final line in registryCalls) {
    buffer.writeln('  $line');
  }
  buffer.writeln('}');

  return GenerationResult(buffer.toString(), widgets);
}

class _AdapterResult {
  final GeneratedWidget summary;
  final String? body;
  final String? registryLine;
  /// Aggregated requiredImports from every encoding that contributed
  /// to this adapter's body. Generator-level accumulator pulls these
  /// up + dedupes across all adapters in the file.
  final List<String> requiredImports;
  _AdapterResult(
    this.summary, {
    this.body,
    this.registryLine,
    this.requiredImports = const [],
  });
}

_AdapterResult _emitAdapter(ClassElement2 cls, ConstructorElement2 ctor) {
  final className = cls.name3 ?? '';
  final ctorName = ctor.name3 ?? '';
  final isDefaultCtor = ctorName.isEmpty || ctorName == 'new';

  // Generic widgets — `class Foo<T> extends StatelessWidget` — need
  // a concrete type argument that codegen can't pick on its own.
  // Most generic Flutter widgets (Consumer, Selector, AnimatedBuilder)
  // also take a `Widget Function(BuildContext, T, …)` builder, which
  // we couldn't encode anyway. Skip with a clear reason so the build
  // log surfaces what got filtered.
  if (cls.typeParameters2.isNotEmpty) {
    final typeParamNames =
        cls.typeParameters2.map((t) => t.name3 ?? '?').join(', ');
    return _AdapterResult(GeneratedWidget(
      className,
      _camelCase(className),
      skipReason: 'generic widget class (type params: <$typeParamNames>) '
          '— codegen can\'t pick concrete type arguments. Wrap manually '
          'with a non-generic Dart subclass that fixes the type, then '
          'add THAT class to your codegen scope.',
    ));
  }

  // JSX-facing symbol. Default ctor: keep the class name (`Shimmer`).
  // Named ctor: concatenate PascalCased (`ShimmerFromColors`). This
  // double-counts a class with both a default AND a named ctor — they
  // get separate JSX symbols + registry keys, which is the point.
  final synthesizedName = isDefaultCtor
      ? className
      : '$className${_pascalCase(ctorName)}';
  final registryKey = _camelCase(synthesizedName);

  // Walk named parameters. Three outcomes per param:
  //
  //   • Supported type → emit a `name: <reader>` line.
  //   • Unsupported, but optional (has a default or is nullable) →
  //     SILENTLY omit. The constructor's own default takes effect.
  //     The dev's JSX can't set this prop, but the widget still works
  //     with sensible defaults from the original Flutter source.
  //   • Unsupported AND required → can't construct the widget without
  //     this param. Skip the whole widget with a clear reason.
  //
  // Tracking [omittedOptionalParams] lets the CLI surface "12 props
  // mapped, 3 omitted (padding, embeddedImage, errorStateBuilder)" so
  // devs know what they can't drive from JSX.
  final omittedOptionalParams = <String>[];
  final requiredImports = <String>{};
  String? skipReason;

  // Two separate lists: positional and named. Dart constructor call
  // syntax requires positional args FIRST (in declaration order),
  // then named args. Emitting in one list with positions interleaved
  // would produce uncompilable code.
  final positionalLines = <String>[];
  final namedLines = <String>[];
  for (final param in ctor.formalParameters) {
    final name = param.name3;
    if (name == null || name.isEmpty) continue;
    if (name == 'key') continue; // skip Flutter's universal `Key? key`

    // FormalParameterElement exposes the default-value expression
    // both as source text (for types we can paste verbatim — strings,
    // numbers) AND as an evaluated constant value (for types we need
    // to inspect — Color's `.value` int, future enums' index, etc.).
    // The type mapper picks whichever it needs per-type.
    //
    // computeConstantValue() returns null when:
    //   • The parameter has no default (we pass null literal too).
    //   • The default isn't a constant expression (a non-const
    //     factory call etc. — unusual for Widget constructors but
    //     theoretically possible). The mapper falls back to defaults
    //     in that case.
    final defaultLiteral = param.defaultValueCode;
    final defaultConstant = param.computeConstantValue();
    final encoding = encodingFor(
      type: param.type,
      paramName: name,
      defaultLiteral: defaultLiteral,
      defaultConstant: defaultConstant,
    );
    if (encoding == null) {
      // Required = no default value. For positional params this is
      // `Foo(this.x)`; for named it's `required this.x`. If the
      // constructor demands it and we can't encode it, we have to
      // skip the widget — the dev needs a hand-written adapter.
      //
      // For OPTIONAL POSITIONAL params with unsupported types we
      // also skip — there's no safe omission rule that preserves
      // the rest of the positional list's position semantics.
      // (Conceivably we could omit a trailing optional positional,
      // but the analyzer's iteration order makes that fragile.)
      final isRequired = (param.isRequiredNamed && defaultLiteral == null) ||
          param.isRequiredPositional;
      if (isRequired) {
        skipReason = "required param '$name' has unsupported type "
            "'${param.type.getDisplayString()}'";
        break;
      }
      if (param.isPositional) {
        skipReason =
            "optional positional param '$name' has unsupported type "
            "'${param.type.getDisplayString()}' (positional omission "
            "would shift later args)";
        break;
      }
      // Optional named: silently omit. Track for CLI reporting.
      omittedOptionalParams.add(name);
      continue;
    }
    if (param.isPositional) {
      // No `name:` prefix — positional args are matched by ORDER on
      // the call site. The JSX-side prop still uses the param's
      // declared name (which we passed to encodingFor as paramName),
      // so the dev's API stays clean: `<Padding paddingLeft={...} />`
      // sets the positional `padding` arg through its sub-props.
      positionalLines.add('    ${encoding.readerExpression},');
    } else {
      namedLines.add('    $name: ${encoding.readerExpression},');
    }
    requiredImports.addAll(encoding.requiredImports);
  }
  final lines = [...positionalLines, ...namedLines];

  if (skipReason != null) {
    return _AdapterResult(
      GeneratedWidget(synthesizedName, registryKey, skipReason: skipReason),
    );
  }

  // Constructor invocation: `Foo(...)` for default, `Foo.bar(...)` for
  // named. The function name uses the synthesized symbol (so a class
  // with multiple ctors gets distinct `_build_Foo` / `_build_FooBar`
  // helpers — no collisions).
  final ctorInvocation = isDefaultCtor ? className : '$className.$ctorName';
  final body = StringBuffer()
    ..writeln(
        'Widget _build_$synthesizedName(NodeState n, SkalBridge bridge) {')
    ..writeln('  return $ctorInvocation(')
    ..writeAll(lines.map((l) => '$l\n'))
    ..writeln('  );')
    ..write('}');

  return _AdapterResult(
    GeneratedWidget(
      synthesizedName,
      registryKey,
      omittedOptionalParams: omittedOptionalParams,
    ),
    body: body.toString(),
    registryLine:
        "SkalRegistry.registerWidget('$registryKey', _build_$synthesizedName);",
    requiredImports: requiredImports.toList(),
  );
}

