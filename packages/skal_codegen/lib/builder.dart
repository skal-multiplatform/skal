// build_runner integration entry point.
//
// Lets devs run `dart run build_runner build` (or `… watch`) to invoke
// skal_codegen the same way they'd invoke any other Dart code
// generator. Standard ecosystem ergonomics: tooling integration, IDE
// hooks, file watching, parallel build steps.
//
// How to opt in (consumer side):
//
//   1. Add to pubspec.yaml:
//        dev_dependencies:
//          build_runner: ^2.4.0
//          skal_codegen: { path: ../packages/skal_codegen }
//
//   2. Create `lib/skal_codegen.yaml` (the marker file) containing:
//        packages:
//          - qr_flutter
//          - video_player
//
//   3. Run `dart run build_runner build`.
//
//   4. Import the generated file from main.dart:
//        import 'skal_codegen.g.dart' as gen;
//        gen.registerAll();
//
// Why a marker file rather than reading the consumer's pubspec.yaml?
//
//   build_runner's output-path derivation is suffix-based — each
//   builder declares "input extension → output extension" pairs, and
//   the engine computes the output path by substituting the input's
//   trailing match. That works cleanly when every input file maps to
//   a paired output ("`.dart` → `.freezed.dart`" alongside).
//
//   Using `$lib$` (build_runner's "synthetic per-package input")
//   forces an awkward path resolution where build_runner's
//   computed expected output path didn't match what we'd write —
//   spent significant time fighting double-`lib/` issues. The marker
//   file pattern is the standard ecosystem alternative + works
//   out-of-the-box with no path tricks.
//
// What we generate:
//
//   For each package listed in skal_codegen.yaml, the Builder
//   resolves its lib/ via the consumer's
//   `.dart_tool/package_config.json`, walks every .dart file, runs
//   the generator, and combines the adapters from all packages into
//   ONE output file (`lib/skal_codegen.g.dart`). Single registerAll(),
//   single import in main.dart.
//
// Limitations of this MVP:
//
//   • Watch mode picks up changes to skal_codegen.yaml automatically
//     (it's the declared input), but NOT to pubspec.lock or the
//     wrapped packages' source files. Workaround: re-run
//     `build_runner build` after `flutter pub upgrade`.
//
//   • Every build regenerates from scratch — no incremental.
//     Acceptable: an analyzer pass over a single package takes ~1s.

// `libraryElement2` is marked @experimental until the analyzer's new
// element model stabilizes. See packages/skal_codegen/lib/src/type_mapper.dart
// for the full rationale.
// ignore_for_file: experimental_member_use

